    _instance: Optional[ChinaTradingCalendar] = None

    def __init__(self, holidays_file: Optional[str] = None):
        self._holidays = set(self._HOLIDAYS)
        self._makeup_days = set(self._MAKEUP_DAYS)
        if holidays_file and Path(holidays_file).exists():
            with open(holidays_file, "r") as f:
                from infra.serialization_utils import json_loads
                data = json_loads(f.read())
            self._holidays.update(data.get("holidays", []))
            self._makeup_days.update(data.get("makeup_days", []))

    @classmethod
    def get_instance(cls) -> ChinaTradingCalendar:
        if cls._instance is None:
            cls._instance = cls()
        return cls._instance

    def is_trading_day(self, d: date) -> bool:
        ds = d.strftime("%Y-%m-%d")
        if ds in self._holidays:
            return False
        if ds in self._makeup_days:
            return True
        return d.weekday() < 5

    def trading_days_between(self, start: date, end: date) -> int:
        if start >= end:
            return 0
        dates = pd.date_range(start, end - timedelta(days=1), freq="D")
        is_weekday = dates.weekday < 5
        is_holiday = np.array(
            [d.strftime("%Y-%m-%d") in self._holidays for d in dates]
        )
        is_makeup = np.array(
            [d.strftime("%Y-%m-%d") in self._makeup_days for d in dates]
        )
        trading = is_weekday & ~is_holiday | is_makeup
        return int(trading.sum())

    def vectorized_days_to_expiry(
        self, bar_dates: np.ndarray, expire_dates: np.ndarray
    ) -> np.ndarray:
        n = len(bar_dates)
        result = np.full(n, -1, dtype=np.int32)
        cal = self.get_instance()
        for i in range(n):
            try:
                bd = pd.Timestamp(bar_dates[i]).date()
                ed = pd.Timestamp(expire_dates[i]).date()
                if ed > bd:
                    result[i] = cal.trading_days_between(bd, ed)
                elif ed == bd:
                    result[i] = 0
            except (ValueError, TypeError, OSError):
                result[i] = -1
        return np.clip(result, -1, 3650)