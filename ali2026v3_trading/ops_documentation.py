"""
## 系统容错策略总览

### 1. 断路器机制 (Circuit Breaker)
- **触发条件**: 连续3次失败 或 日回撤超过阈值
- **状态**: CLOSED(正常) → OPEN(断开) → HALF_OPEN(半开试探)
- **恢复**: HALF_OPEN状态下连续2次成功自动恢复CLOSED
- **持久化**: 状态实时持久化到JSON文件，崩溃重启后恢复
- **实现**: risk_service.py SafetyMetaLayer._save_circuit_breaker_state()

### 2. 降级策略 (Degradation)
- **DEGRADED状态**: 核心服务未初始化完成时进入
- **阻断**: DEGRADED状态下tick回调直接return，不处理交易
- **恢复**: 服务初始化完成后自动恢复正常状态
- **实现**: strategy_core_service.py L2100-2104

### 3. 补偿事务 (Compensating Transaction)
- **场景**: 批量下单(iceberg/twap/smart)中途失败
- **策略**: 逆序撤回已提交的子单
- **实现**: order_service.py _execute_with_compensation_v2()

### 4. 幂等去重 (Idempotency)
- **场景**: 下单重试、网络超时重连
- **策略**: client_order_id去重集合，重复请求直接跳过
- **持久化**: JSONL追加写入，崩溃重启后恢复
- **实现**: order_service.py _order_idempotent_set

### 5. WAL预写日志 (Write-Ahead Log)
- **场景**: tick订阅管理
- **覆盖**: subscribe请求、重试队列
- **恢复**: 启动时从WAL恢复订阅状态
- **实现**: subscription_manager.py _recover_from_wal()

### 6. 订单/持仓持久化
- **策略**: JSONL追加写入，每次状态变更落盘
- **恢复**: 启动时从JSONL恢复最新状态
- **实现**: order_service.py _append_order_state(), position_service.py _append_position_state()

### 7. 数据完整性校验
- **tick校验**: bid/ask价差、price>0、volume异常检测
- **Bar校验**: OHLC关系检查
- **WAL校验**: CRC32校验和
- **DuckDB**: PRAGMA integrity_check启动时执行

### 8. 混沌工程验证
- **框架**: chaos_engineering.py
- **注入类型**: 异常、延迟、网络分区、磁盘满
- **实验定义**: ChaosExperiment.run_experiment(hypothesis, fault_fn, verify_fn)
"""

